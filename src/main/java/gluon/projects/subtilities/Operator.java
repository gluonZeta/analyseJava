package gluon.projects.subtilities;

public class Operator {
    public boolean ternaryOperator(boolean b) {
        if(b) return false;
        else return b;
    }

    public int addition(int a, int b) {
        return a+b;
    }

    public int incrementation() {
        int x = 5;
        x++;
        return x;
    }
}
